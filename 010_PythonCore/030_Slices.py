str1 = '1234567890'

# Срезы 2-аргумента
print(str1[:5])
print(str1[5:])
print(str1[-5:])
print(str1[-5:7])
print(str1[:5] + "_===_" + str1[5:])

# Срезы 3-аргумента
print(str1[::3])    # шаг 3
print(str1[::-1])   # инверсия, с начала до конца с шагом -1, т.е. 0-1=-1, 0-2=-2, .... -10
print(str1[::-2])
print(str1[:2:-1])
print(str1[:2:-2])
